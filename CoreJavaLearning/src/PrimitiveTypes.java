public class PrimitiveTypes {
    public static void main(String[] args) {
        char ch='\u00A3';
        System.out.println("Unicode Character = " + ch);
        char ch1='\u00AF';
        System.out.println("Unicode Character = " + ch1);
        char ch2='\u0032';
        System.out.println("Unicode Character = " + ch2);
        char ch3='\u002E';
        System.out.println("Unicode Character = " + ch3);
        char ch4='\u008A';
        System.out.println("Unicode Character = " + ch4);
    }
}
